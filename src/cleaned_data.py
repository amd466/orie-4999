def make_unique(series):
    counts, out = {}, []
    for n in series:
        base = str(n).strip()
        counts[base] = counts.get(base, 0) + 1
        # First occurrence stays as-is; later ones get (2), (3), ...
        out.append(base if counts[base] == 1 else f"{base} ({counts[base]})")
    return out

