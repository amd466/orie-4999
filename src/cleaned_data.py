clean = []
for _, row in df.iterrows():
    name = str(row.get(name_col, "None")).strip()
    email = str(row.get(email_col, "None")).strip()
    role_raw = str(row.get(role_col, "None")).strip().lower()

    if "pccw" in role_raw:
        role = "Mentor"; student_level = None
