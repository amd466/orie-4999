def parse_rank(val):
    if isinstance(val, str):
        m = re.search(r'(\d+)', val)
        if m:
            i = int(m.group(1))
            if 1 <= i <= 5:
                return i
    return np.nan

def normalize_multiselect(val, reference_list):
    if not isinstance(val, str) or val.strip() == "":
        return ["None"]
    parts = re.split(r"[;,]", val)
    cleaned = []
    for p in parts:
        t = p.strip()
        if not t:
            continue
        t_title = t.title().replace("U.s.", "U.S.").replace("Nyc", "NYC")
        low = t_title.lower()
        match = None
        for ref in reference_list:
            if low in ref.lower() or ref.lower() in low:
                match = ref
                break
        cleaned.append(match if match else t_title)
    return cleaned if cleaned else ["None"]

def merge_organization_fields(row):
    vals = []
    if org_primary_col:
        v1 = str(row.get(org_primary_col, "")).strip()
        if v1:
            vals.append(v1)
    if org_grad_col:
        v2 = str(row.get(org_grad_col, "")).strip()
        if v2:
            vals.append(v2)
    if not vals:
        return ["None"]
    merged = ", ".join(vals)
    return normalize_multiselect(merged, ORGANIZATIONS)

