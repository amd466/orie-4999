def find_contains_all(*subs):
    for c in df.columns:
        lc = c.lower()
        if all(s.lower() in lc for s in subs):
            return c
    return None

def find_prefix(prefix):
    for c in df.columns:
        if c.strip().lower().startswith(prefix.strip().lower()):
            return c
    return None

